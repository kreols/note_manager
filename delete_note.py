def display_notes(notes):
    """Функция для отображения списка заметок."""
    if not notes:
        print("Заметок нет.")
        return

    print("\nТекущие заметки:")
    for i, note in enumerate(notes, start=1):
        print(f"{i}. Имя: {note['Имя']}")
        print(f"   Заголовок: {note['Заголовок']}")
        print(f"   Описание: {note['Описание']}\n")


def delete_note(notes):
    """Функция для удаления заметок по имени пользователя или заголовку."""
    if not notes:
        print("Список заметок пуст. Удалять нечего.")
        return

    # Запрашиваем критерий для удаления
    criteria = input("Введите имя пользователя или заголовок для удаления заметки: ")

    # Удаляем заметки, соответствующие критерию
    initial_count = len(notes)
    notes[:] = [note for note in notes if note["Имя"] != criteria and note["Заголовок"] != criteria]

    # Проверяем, были ли удалены заметки
    if len(notes) == initial_count:
        print(f"Заметок с именем пользователя или заголовком '{criteria}' не найдено.")
    else:
        print(f"Успешно удалено. Остались следующие заметки:")
        display_notes(notes)


def main():
    # Пример списка заметок
    notes = [
        {"Имя": "Алексей", "Заголовок": "Список покупок", "Описание": "Купить продукты на неделю"},
        {"Имя": "Мария", "Заголовок": "Учеба", "Описание": "Подготовиться к экзамену"},
    ]

    # Отображаем текущие заметки
    display_notes(notes)

    # Вызываем функцию удаления
    delete_note(notes)


if __name__ == "__main__":
    main()