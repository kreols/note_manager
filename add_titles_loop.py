def add_titles_loop():
    """
    Запрашивает у пользователя заголовки заметок и выводит список кортежей.
    Позволяет пользователю добавлять заголовки до тех пор, пока не будет введен пустой ввод.
    """
    titles = []
    while True:
        title = input("Введите заголовок (или оставьте пустым для завершения): ")
        if not title:
            break
        titles.append((title,))  # Добавляем заголовок как кортеж

    print("\nЗаголовки заметки:")
    for title_tuple in titles:
        print(f"- {title_tuple[0]}")  # Выводим первый (и единственный) элемент кортежа

if __name__ == "__main__":
    add_titles_loop()

